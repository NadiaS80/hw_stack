import unittest
from stack_balance_check import balanced

class TestStack(unittest.TestCase):

    def test_balanced(self):
        params = [
    ('[([])((([[[]]])))]{()}', 'Сбалансированно'),
    ('(((([{}]))))', 'Сбалансированно'),
    ('{{[()]}}', 'Сбалансированно'),
    ('}{}', 'Несбалансированно'),
    ('{{[(])]}}', 'Несбалансированно'),
    ('[[{())}]', 'Несбалансированно'),
    ('(((', 'Несбалансированно'),
    ('()', 'Сбалансированно'),
    ('[]', 'Сбалансированно'),
    ('{}', 'Сбалансированно'),
    ('()[]{}', 'Сбалансированно'),
    ('{[()]}', 'Сбалансированно'),
    ('{{[()]}}', 'Сбалансированно'),
    ('(((([{}]))))', 'Сбалансированно'),
    ('[([])((([[[]]])))]{()}', 'Сбалансированно'),
    ('{[()()([])]}', 'Сбалансированно'),
    ('(([]){})', 'Сбалансированно'),
    ('{([][])}()', 'Сбалансированно'),
    ('[{({[()]})}]', 'Сбалансированно'),
    ('(', 'Несбалансированно'),
    (')', 'Несбалансированно'),
    ('{', 'Несбалансированно'),
    ('}', 'Несбалансированно'),
    ('}{', 'Несбалансированно'),
    (')(', 'Несбалансированно'),
    ('][]', 'Несбалансированно'),
    ('}{}', 'Несбалансированно'),
    (')[]', 'Несбалансированно'),
    ('(]', 'Несбалансированно'),
    ('[)', 'Несбалансированно'),
    ('{)', 'Несбалансированно'),
    ('([)]', 'Несбалансированно'),
    ('{[(])}', 'Несбалансированно'),
    ('{{[(])]}}', 'Несбалансированно'),
    ('((', 'Несбалансированно'),
    ('{{', 'Несбалансированно'),
    ('[{(', 'Несбалансированно'),
    ('(((()', 'Несбалансированно'),
    ('{[(', 'Несбалансированно'),
    ('', 'Сбалансированно'),
]

        for i, (the_str, correct) in enumerate(params):
            with self.subTest(i):
                result = balanced(the_str)
                self.assertEqual(result, correct)


if __name__ == '__main__':
    unittest.main()
